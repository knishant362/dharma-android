import com.aurora.app.R

val provider = GoogleFont.Provider(
    providerAuthority = "com.google.android.gms.fonts",
    providerPackage = "com.google.android.gms",
    certificates = R.array.com_google_android_gms_fonts_certs
)

fun calistogaFontFamily(): FontFamily {
    val googleFont = GoogleFont("Calistoga")
    return FontFamily(
        Font(googleFont, provider, FontWeight.Normal)
    )
}

fun nunitoFontFamily(): FontFamily {
    val googleFont = GoogleFont("Nunito")
    return FontFamily(
        Font(googleFont, provider, FontWeight.Light),
        Font(googleFont, provider, FontWeight.Normal),
        Font(googleFont, provider, FontWeight.Bold)
    )
}

